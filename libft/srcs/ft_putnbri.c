#include "libft.h"

void	ft_putnbri(int *n, int i)
{
	int a;

	a = 0;
	while (i > a)
	{
		ft_putnbr(n[a]);
		if (i > ++a)
			write(1, ", ", 2);
	}
	write(1, "\n", 1);
}
